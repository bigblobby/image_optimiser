import ReactGA from 'react-ga';

class GoogleAnalytics {
    init(){
        ReactGA.initialize('UA-166789742-1');
    }

    set(options){
        ReactGA.set(options);
    }

    pageview(path){
        ReactGA.pageview(path);
    }
}

const ga = new GoogleAnalytics();
ga.init();
export default ga;
